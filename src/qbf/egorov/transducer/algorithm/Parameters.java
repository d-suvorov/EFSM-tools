package qbf.egorov.transducer.algorithm;

/**
 * @author kegorov
 *         Date: Apr 1, 2010
 */
public class Parameters {
    private boolean fixedOutput;
    private int populationSize;
    private double desiredFitness;
    private int stateNumber;
    private double partStay;
    private int timeSmallMutation;
    private int timeBigMutation;
    private double mutationProbability;

    public boolean isFixedOutput() {
        return fixedOutput;
    }

    public void setFixedOutput(boolean fixedOutput) {
        this.fixedOutput = fixedOutput;
    }

    public int getPopulationSize() {
        return populationSize;
    }

    public void setPopulationSize(int populationSize) {
        this.populationSize = populationSize;
    }

    public double getDesiredFitness() {
        return desiredFitness;
    }

    public void setDesiredFitness(double desiredFitness) {
        this.desiredFitness = desiredFitness;
    }

    public int getStateNumber() {
        return stateNumber;
    }

    public void setStateNumber(int stateNumber) {
        this.stateNumber = stateNumber;
    }

    public double getPartStay() {
        return partStay;
    }

    public void setPartStay(double partStay) {
        this.partStay = partStay;
    }

    public int getTimeSmallMutation() {
        return timeSmallMutation;
    }

    public void setTimeSmallMutation(int timeSmallMutation) {
        this.timeSmallMutation = timeSmallMutation;
    }

    public int getTimeBigMutation() {
        return timeBigMutation;
    }

    public void setTimeBigMutation(int timeBigMutation) {
        this.timeBigMutation = timeBigMutation;
    }

    public double getMutationProbability() {
        return mutationProbability;
    }

    public void setMutationProbability(double mutationProbability) {
        this.mutationProbability = mutationProbability;
    }
}
